CREATE PROCEDURE BOBBY_TABLES.GET_ID_PAIS(@PAIS VARCHAR(250)) AS
BEGIN
	DECLARE @PAIS_ID INT
	
	SELECT @PAIS_ID = PAI_ID FROM BOBBY_TABLES.PAISES
	WHERE @PAIS = NOMBRE
	
	IF @PAIS_ID IS NULL
	BEGIN
		INSERT INTO BOBBY_TABLES.PAISES
		VALUES(@PAIS)
		SET @PAIS_ID = @@IDENTITY
	END
	
	RETURN @PAIS_ID
END

