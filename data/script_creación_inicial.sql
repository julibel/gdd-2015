GO
CREATE PROCEDURE BOBBY_TABLES.MODIFICAR_CLIENTE(@ID INT,@NOMBRE VARCHAR(255),@APELLIDO VARCHAR(255),
	@DOC NUMERIC(10,0),@TIPO_DOC INT,@CALLE VARCHAR(255),@NUMERO_DOM NUMERIC(5,0),@PISO INT,@DEPTO VARCHAR(10),
	@LOCALIDAD VARCHAR(60),@PAIS VARCHAR(250),@FECHA_NAC_APP VARCHAR(30),@NACIONALIDAD VARCHAR(250),@MAIL VARCHAR(255),
	@USERNAME VARCHAR(40),@PASSWORD VARBINARY(32),@FECHA_MOD_APP VARCHAR(30),@PREGUNTA VARCHAR(100),@RESPUESTA VARBINARY(32)) AS
BEGIN
	DECLARE @USUARIO INT, @PAIS_ID INT, @NAC_ID INT, @FECHA_NAC DATETIME, @FECHA_MOD DATETIME
	SET @FECHA_NAC = CONVERT(DATETIME, @FECHA_NAC_APP)
	SET @FECHA_MOD = CONVERT(DATETIME, @FECHA_MOD_APP)
	
	SELECT @USUARIO = USUARIO FROM BOBBY_TABLES.CLIENTES WHERE CLI_ID = @ID
	
	EXEC @PAIS_ID = BOBBY_TABLES.GET_ID_PAIS
				@PAIS = @PAIS
				
	EXEC @NAC_ID = BOBBY_TABLES.GET_ID_PAIS
				@PAIS = @NACIONALIDAD
	
	UPDATE BOBBY_TABLES.CLIENTES SET
		NOMBRE = @NOMBRE,
		APELLIDO = @APELLIDO,
		NUMERO_DOC = @DOC,
		TIPO_DOC = @TIPO_DOC,
		CALLE = @CALLE,
		NUMERO_DOM = @NUMERO_DOM,
		PISO = @PISO,
		DEPTO = @DEPTO,
		LOCALIDAD = @LOCALIDAD,
		PAIS = @PAIS,
		FECHA_NACIMIENTO = @FECHA_NAC,
		NACIONALIDAD = @NACIONALIDAD,
		MAIL = @MAIL
	WHERE CLI_ID = @ID
	
	UPDATE BOBBY_TABLES.USUARIOS SET
		USERNAME = @USERNAME,
		PASSWORD = @PASSWORD,
		FECHA_MODIFICACION = @FECHA_MOD,
		PREGUNTA_SECRETA = @PREGUNTA,
		RESPUESTA = @RESPUESTA
	WHERE USU_ID = @USUARIO
END

